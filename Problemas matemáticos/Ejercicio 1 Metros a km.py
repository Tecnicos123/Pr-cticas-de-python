"""
El volcán Barú con una altura de 3475 metros, sobre el nivel del mar,
es el punto máximo de elevación en la república de Panamá.
¿Cuál es su altura en Km?

Para convertir la altura del volcán Barú de metros (m) a kilómetros (km), se debe dividir la cantidad entre 1000, ya que $1\text{ km} = 1000\text{ m}$.

Respuesta: La altura del volcán Barú es de 3.475 kilómetros.
"""

metros = float(input("Ingrese la altura en metros: "))
kilometros = metros / 1000

print(f"{metros} equivalen a {kilometros} kilómetros")