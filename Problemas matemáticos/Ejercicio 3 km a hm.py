"""
La vía interoceánica tiene una longitud total de 80 km. ¿Cuál es su largo en hm? R=800 hm
Para pasar de kilómetros (km) a hectómetros (hm), se multiplica por 10, ya que un kilómetro equivale a 10 hectómetros.
"""
km = float(input("Ingrese la cantidad en km: "))
hm = 10 * km

print(f"{km} equivalen a {hm} hectómetros")