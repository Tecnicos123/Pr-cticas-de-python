"""
La vía interoceánica tiene una longitud total de 80 km. ¿Cuál es su largo en metros?
Para convertir kilómetros a metros, recuerda que:
1 km = 1.000 metros
Por lo tanto:
80 km = 80 × 1.000 = 80.000 metros
"""

km = float(input("Ingrese la cantidad en km: "))
mts = 1000 * km

print(f"{km} equivalen a {mts} metros")



